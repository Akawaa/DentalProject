                            <div class="tab-pane fade" id="resin">
                                <div class="col-md-3">
                                    <br>
                                    <img src="<?php echo base_url();?>assets/images/Fillings/composite-resin.jpg">
                                    <br>
                                </div>
                                <div class="col-md-9">
                                    <br>
                                    <p><strong>Made Of : </strong>A mixture of plastic and fine glass particles.</p>
                                    <p><strong>Types : </strong>Direct and indirect. Direct fillings are placed by your dentist
                                        using a bright blue light that hardens the soft material. For indirect fillings, your dentist
                                        prepares the tooth, takes an impression and a laboratory makes the filling. During a second visit,
                                        your dentist cements this filling into place.</p>
                                    <p><strong>Used for : </strong>Small and large fillings, especially in front teeth or the visible parts of teeth; also for inlays.</p>
                                    <p><strong>Lasts : </strong>At least five years.</p>
                                    <p><strong>Costs : </strong> More than amalgam, but less than gold.</p>
                                </div>
                                <div class="col-md-12">
                                    <p><strong>Advantages : </strong>
                                    <ul>
                                        <li>Your fillings or inlay will match the color of your teeth.</li>
                                        <li>A filling can be completed in one dental visit. An inlay may require two visits.</li>
                                        <li>Composite fillings can bond directly to the tooth, making the tooth stronger than it would be with an amalgam filling.</li>
                                        <li>Less drilling is involved than with amalgam fillings because your dentist does not have to shape the space as much to hold the
                                            filling securely. The bonding process holds the composite resin in the tooth.</li>
                                        <li>Indirect composite fillings and inlays are heat-cured, increasing their strength.</li>
                                        <li>Composite resin can be used in combination with other materials, such as glass ionomer, to provide the benefits of both materials.</li>
                                    </ul>
                                    </p>
                                    <p><strong>Disadvantages : </strong>
                                    <ul>
                                        <li>Although composite resins have become stronger and more resistant to wear, it's not clear whether they are
                                            strong enough to last as long as amalgam fillings under the pressure of chewing</li>
                                        <li>The composite may shrink when placed; this can lead to more cavities in the future in areas where the filling is
                                            not making good contact with your tooth. The shrinkage is reduced when your dentist places this type of filling in thin layers.</li>
                                        <li>These fillings take more time to place because they are usually placed in layers. The increased time and labor involved also contribute
                                            to the higher cost (compared with amalgam fillings).</li>
                                        <li>Indirect fillings and inlays take at least two visits to complete. Your dentist takes impressions at the first visit and places the
                                            filling or inlay at the second visit.</li>
                                        <li>In large cavities, composites may not last as long as amalgam fillings.</li>
                                    </ul>
                                    </p>
                                </div>
