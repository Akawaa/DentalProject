                                    <p><strong>Made Of : </strong>Gold.</p>
                                    <p><strong>Used for : </strong>Small fillings in areas where you don’t chew hard; repairing crowns.</p>
                                    <p><strong>Lasts : </strong>Approximately 10 to 15 years</p>
                                    <p><strong>Costs : </strong> Up to four times as much as amalgam</p>
                                </div>
                                <div class="col-md-12">
                                    <p><strong>Advantages : </strong>
                                    <ul>
                                        <li>Gold foil can last a long time.</li>
                                    </ul>
                                    </p>
                                    <p><strong>Disadvantages : </strong>
                                    <ul>
                                        <li>Gold foil restorations require great skill and attention to detail by the dentist. Improper placement
                                            of gold foil can damage the pulp or periodontal (gum) tissues.</li>
                                        <li>Gold foil costs more than amalgam and composite fillings.</li>
                                        <li>It may be difficult to find a dentist who offers gold foil as an option because it is being replaced
                                            by other materials that match the color of your teeth.
                                        </li>
                                    </ul></p>
