                                    <br>
                                    <p><strong>Made Of : </strong>Gold alloy (gold mixed with other metals).</p>
                                    <p><strong>Used for : </strong>Inlays and onlays, crowns.</p>
                                    <p><strong>Lasts : </strong>At least seven years, usually longer</p>
                                    <p><strong>Costs : </strong> More than most other materials; six to ten times more expensive than amalgam.</p>
                                </div>
                                <div class="col-md-12">
                                    <p><strong>Advantages : </strong>
                                    <ul>
                                        <li>Gold doesn't corrode.</li>
                                        <li>Some people like the gold color better than the silver color of amalgam.</li>
                                        <li>Gold fillings are durable enough to withstand chewing forces.</li>
                                    </ul>
                                    </p>
                                    <p><strong>Disadvantages : </strong>
                                    <ul>
                                        <li>You must visit the dentist at least twice to receive a gold filling. At the first visit, the dentist
                                            makes an impression of your tooth and a temporary filling is placed. The gold filling is made from the
                                            impression and is placed at a second visit.</li>
                                        <li>The cost is high because of the high cost of gold and the work involved.</li>
                                        <li>If gold and amalgam fillings are right next to each other in your mouth, an electric current can result
                                            from interactions between the metals and your saliva, resulting in discomfort. This is called "galvanic shock."
                                        </li>
                                    </ul>
                                    </p>
                                </div>
