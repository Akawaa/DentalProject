                                <div class="col-md-9">
                                    <br>
                                    <p><strong>Made Of : </strong>A mixture of silver, tin, zinc, copper, and mercury. Mercury is nearly 50% of the mixture.</p>
                                    <p><strong>Types : </strong>Traditional (non-bonded), bonded.</p>
                                    <p><strong>Used for : </strong>Fillings in back teeth.</p>
                                    <p><strong>Lasts : </strong>At least seven years, usually longer.</p>
                                    <p><strong>Costs : </strong>The least expensive type of restorative materia.</p>
                                </div>
                                <div class="col-md-12">
                                    <p><strong>Advantages : </strong>
                                    <ul>
                                        <li>Amalgam fillings are strong and can withstand the forces of chewing.</li>
                                        <li>They are relatively inexpensive, compared with alternatives.</li>
                                        <li>An amalgam filling can be completed in one dental visit.</li>
                                    </ul>
                                    </p>
                                    <p><strong>Disadvantages : </strong>
                                    <ul>
                                        <li>Amalgam doesn't match the color of your teeth.</li>
                                        <li>Healthy parts of your tooth often must be removed to make a space large enough to hold an amalgam filling.</li>
                                        <li>Amalgam fillings can corrode or tarnish over time, causing discoloration where the filling meets the tooth.</li>
                                        <li>A traditional (non-bonded) amalgam filling does not bond (stick) to your tooth. The cavity preparation (the "pocket" in your tooth)
                                            developed by your dentist requires undercuts or ledges to provide retention of the filling. Your dentist may have to remove additional
                                            tooth structure to establish good retention for the filling.</li>
                                        <li>Some people may be allergic to mercury or be concerned about its effects, although research shows the amount of mercury exposure from
                                            fillings is similar to what people get from other sources in the environment.</li>
                                    </ul>
                                    </p>
