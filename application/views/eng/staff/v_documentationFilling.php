                                    <p><strong>Made Of : </strong>Porcelain, most commonly.</p>
                                    <p><strong>Used for : </strong>Inlays and onlays, crowns.</p>
                                    <p><strong>Lasts : </strong>Five to seven years.</p>
                                    <p><strong>Costs : </strong>Can cost as little as composite or as much or more than gold, depending on the filling.</p>
                                </div>
                                <div class="col-md-12">
                                    <p><strong>Advantages : </strong>
                                    <ul>
                                        <li>Ceramics are tooth-colored.</li>
                                        <li>Ceramics are more resistant to staining and abrasion than composite resin.</li>
                                    </ul>
                                    </p>
                                    <p><strong>Disadvantages : </strong>
                                    <ul>
                                        <li>Ceramics are more brittle than composite resin.</li>
                                        <li>A ceramic inlay or onlay needs to be large enough to prevent it from breaking, so the tooth must
                                            be reduced in size to make room for the extra bulk.</li>

                                    </ul></p>
