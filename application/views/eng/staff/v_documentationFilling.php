                                    <p><strong>Made Of : </strong>Acrylic and a component of glass called fluoroaluminosilicate.</p>
                                    <p><strong>Used for : </strong>Most commonly used as cementation for inlay fillings, but glass ionomer
                                        also is used for fillings in front teeth or to fill areas around the necks of your teeth or on roots.
                                        As filling material, glass ionomer is typically used in people with a lot of decay in the part of the
                                        tooth that extends below the gum (root caries). It is also used for filling baby teeth and as a liner
                                        for other types of fillings.</p>
                                    <p><strong>Lasts : </strong>Five years or more.</p>
                                    <p><strong>Costs : </strong>Comparable to composite resin.</p>
                                </div>
                                <div class="col-md-12">
                                    <p><strong>Advantages : </strong>
                                    <ul>
                                        <li>Glass ionomer matches the color of the teeth, although it does not always match as well as
                                            composite resin. Resin-modified glass ionomer is usually a better match than traditional glass
                                            ionomer.</li>
                                        <li>In some cases, no drilling is needed to place a glass ionomer filling. This makes this type of
                                            filling useful for small children.</li>
                                        <li>Glass ionomers release fluoride, which can help protect the tooth from further decay.</li>
                                        <li>Glass ionomer restorations bond (stick) to the tooth, which helps to prevent leakage around the
                                            filling and further decay.</li>
                                    </ul>
                                    </p>
                                    <p><strong>Disadvantages : </strong>
                                    <ul>
                                        <li>Traditional glass ionomer is significantly weaker than composite resin. It is much more likely
                                            to wear or fracture.</li>
                                        <li>Traditional glass ionomer does not match your tooth color as precisely as composite resin.</li>
                                        <li>If you are getting a resin-modified glass ionomer filling, each thin layer needs to be cured,
                                            or hardened, with a special bright blue light before the next layer can be added. This makes
                                            the tooth stronger, but can lengthen the time of the dental appointment.</li>

                                    </ul></p>
